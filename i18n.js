window.I18N = {
  currentLang: 'es', // Por defecto

  init: function() {
    let savedLang = localStorage.getItem('pp_lang');
    
    if (!savedLang) {
      const browserLang = (navigator.language || navigator.userLanguage).slice(0, 2).toLowerCase();
      if (['es', 'en', 'ru', 'zh'].includes(browserLang)) {
        savedLang = browserLang;
      } else {
        savedLang = 'es';
      }
    }
    this.setLanguage(savedLang);
  },

  setLanguage: function(lang) {
    if (!translations[lang]) lang = 'es'; // Fallback
    console.log("I18N: Cambiando idioma a ->", lang);
    
    this.currentLang = lang;
    localStorage.setItem('pp_lang', lang);
    // Emit message to child iframes
    for(let i=0; i<window.frames.length; i++){
      try { window.frames[i].postMessage({type: 'languageChanged', lang: lang}, '*'); } catch(e){}
    }
    // Emit message to child iframes
    for(let i=0; i<window.frames.length; i++){
      try { window.frames[i].postMessage({type: 'languageChanged', lang: lang}, '*'); } catch(e){}
    }
    document.documentElement.lang = lang; 

    const dict = translations[lang];

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
             el.placeholder = dict[key];
        } else {
          // Usamos innerHTML para mayor compatibilidad
          el.innerHTML = dict[key];
        }
      }
    });

    // Evento manual por si otros componentes necesitan percatarse
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: lang } }));
  },
  
  getDict: function() {
      return translations[this.currentLang] || translations['es'];
  }
};

// Autoinicialización robusta
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => window.I18N.init());
} else {
    window.I18N.init();
}


// Listen for cross-tab or cross-iframe language changes
window.addEventListener('storage', function(e) {
    if (e.key === 'pp_lang') {
        const newLang = e.newValue || 'es';
        if (window.I18N && window.I18N.currentLang !== newLang) {
            window.I18N.setLanguage(newLang);
        }
    }
});

// Also listen for postMessage from parent iframe (just in case they are different origins, though they are same origin here)
window.addEventListener('message', function(e) {
    if (e.data && e.data.type === 'languageChanged' && e.data.lang) {
        if (window.I18N && window.I18N.currentLang !== e.data.lang) {
            window.I18N.currentLang = e.data.lang;
            window.I18N.updateDOM();
        }
    }
});
