window.I18N = {
    "currentLang": 'en',
    "init": function() {
    const supportedLangs = ['es', 'en', 'ru', 'zh'];
    const savedLang = localStorage.getItem('pp_lang');

    if (savedLang && supportedLangs.includes(savedLang)) {
      this.setLanguage(savedLang);
      return;
    }

    const languageCandidates = [];
    try {
      if (Array.isArray(navigator.languages)) {
        navigator.languages.forEach(lang => languageCandidates.push(lang));
      }
      if (navigator.language) languageCandidates.push(navigator.language);
      if (navigator.userLanguage) languageCandidates.push(navigator.userLanguage);
    } catch(e){}

    const normalized = languageCandidates
      .filter(Boolean)
      .map(lang => String(lang).slice(0, 2).toLowerCase());

    const deviceLang = normalized.find(lang => supportedLangs.includes(lang)) || 'en';
    this.setLanguage(deviceLang);
  },
    "setLanguage": function(lang) {
    if (!translations[lang]) lang = 'en';
    console.log("I18N: Cambiando idioma a ->", lang);
    
    this.currentLang = lang;
    localStorage.setItem('pp_lang', lang);
    // Emit message to child iframes
    for(let i=0; i<window.frames.length; i++){
      try { window.frames[i].postMessage({type: 'languageChanged', lang: lang}, '*'); } catch(e){}
    }
    // Emit message to child iframes
    for(let i=0; i<window.frames.length; i++){
      try { window.frames[i].postMessage({type: 'languageChanged', lang: lang}, '*'); } catch(e){}
    }
    document.documentElement.lang = lang; 

    this.updateDOM();

    // Evento manual por si otros componentes necesitan percatarse
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: lang } }));
  },
    "updateDOM": function() {
  const dict = translations[this.currentLang] || translations['en'];

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (!key || !dict[key]) return;
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = dict[key];
      } else if (el.tagName === 'OPTION') {
        el.textContent = dict[key];
      } else if (el.children.length > 0 && el.childNodes.length === 1) {
        el.textContent = dict[key];
      } else if (el.children.length > 0) {
        let replaced = false;
        el.childNodes.forEach(node => {
          if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
            node.textContent = dict[key];
            replaced = true;
          }
        });
        if (!replaced) {
          el.textContent = dict[key];
        }
      } else {
        el.textContent = dict[key];
      }
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const key = el.getAttribute('data-i18n-ph');
      if (!key || !dict[key]) return;
      el.setAttribute('placeholder', dict[key]);
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria-label');
      if (!key || !dict[key]) return;
      el.setAttribute('aria-label', dict[key]);
    });

    if (window.PPAuth && typeof window.PPAuth.ensureLogoutButton === 'function') {
      window.PPAuth.ensureLogoutButton({ selector: '[data-pp-logout]' });
    }
  },
    "getDict": function() {
      return translations[this.currentLang] || translations['en'];
  }
};

// Autoinicialización robusta
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => window.I18N.init());
} else {
    window.I18N.init();
}


// Listen for cross-tab or cross-iframe language changes
window.addEventListener('storage', function(e) {
    if (e.key === 'pp_lang') {
    const newLang = e.newValue || 'en';
        if (window.I18N && window.I18N.currentLang !== newLang) {
            window.I18N.setLanguage(newLang);
        }
    }
});

// Also listen for postMessage from parent iframe (just in case they are different origins, though they are same origin here)
window.addEventListener('message', function(e) {
    if (e.data && e.data.type === 'languageChanged' && e.data.lang) {
        if (window.I18N && window.I18N.currentLang !== e.data.lang) {
            window.I18N.currentLang = e.data.lang;
            window.I18N.updateDOM();
        }
    }
});
